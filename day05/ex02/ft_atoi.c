int		ft_atoi(char *str)
{
	int res;
	int flag;

	res = 0;
	flag = 0;
	while (*str == '\n' || *str == ' ' || *str == '\t' || *str == '\r' ||
		*str == '\v' || *str == '\f')
		++str;
	if (*str == '+' || *str == '-')
	{
		if (*str == '-')
			flag = 1;
		str++;
	}
	while (*str != '\0' && *str >= '0' && *str <= '9')
	{
		if (*str >= '0' && *str <= '9')
			res = res * 10 + (flag ? -(*str - '0') : *str - '0');
		str++;
	}
	return (res);
}
